package com.xwork.math;
// Check whether a number is even or odd
public class EvenOddCheck {
    public static void main(String[] args) {
        int num=3;

        if (num%2==0)
        {
            System.out.println("the given No "+num + " is an even number.");
        }
        else
        {
            System.out.println("the given No "+num + " is an odd number.");
        }
    }
}
